package com.boundary.plugin.sdk.examples;

public class SimpleConfiguration {
	
	private int delay;
	private String name;
	
	SimpleConfiguration() {
		
	}
	
	public int getDelay() {
		return delay;
	}
	public void setDelay(int delay) {
		this.delay = delay;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
